#Lists are ordered, mutable sequences that store multiple values and support indexing and slicing.

my_list = ['a', 'b', 'c', 'd', 'e']

#Adding an element to the end of the list
my_list.append('f')
print(my_list)

#Accessing a range of elements by index
print(my_list[0:3])

#['a', 'b', 'c', 'd', 'e', 'f']
#['a', 'b', 'c']